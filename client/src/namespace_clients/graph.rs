#[derive(Default)]
pub struct GraphExplore {
    client: Elasticsearch,
    error_trace: Option<bool>,
    filter_path: Option<Vec<String>>,
    human: Option<bool>,
    pretty: Option<bool>,
    source: Option<String>,
    routing: Option<String>,
    timeout: Option<String>,
}
